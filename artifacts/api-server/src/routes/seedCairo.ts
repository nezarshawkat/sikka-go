// ─── Helper: build line records from a stops array ───────────────────────────
function mkLine(
  typeKey: string, lineNum: string, nameArPrefix: string,
  stops: string[], price: number, freq: number, acPrice?: number,
): LineSpec[] {
  const from = stops[0];
  const to   = stops[stops.length - 1];
  const via  = stops.slice(1, -1);
  return [{
    typeKey, lineNum,
    nameEn: `${lineNum}: ${from} → ${to}`,
    nameAr: `${nameArPrefix} ${lineNum}: ${from} - ${to}`,
    from, to, via, price: acPrice ?? price, freq,
    hasFixedStops: typeKey === "Metro" || typeKey === "Monorail" || typeKey === "Train",
  }];
}

function pairs(
  typeKey: string, code: string, arPrefix: string,
  stations: string[], price: number, freq: number,
): LineSpec[] {
  const res: LineSpec[] = [];
  for (let i = 0; i < stations.length - 1; i++) {
    res.push({
      typeKey, lineNum: `${code}-${String(i + 1).padStart(2, "0")}`,
      nameEn: `${code}: ${stations[i]} → ${stations[i + 1]}`,
      nameAr: `${arPrefix}: ${stations[i]} - ${stations[i + 1]}`,
      from: stations[i], to: stations[i + 1], via: [],
      price, freq, hasFixedStops: true,
    });
  }
  return res;
}

interface LineSpec {
  typeKey: string; lineNum: string;
  nameEn: string; nameAr: string;
  from: string; to: string; via: string[];
  price: number; freq: number;
  hasFixedStops: boolean;
}

