// MAIN SEED HANDLER
// ═══════════════════════════════════════════════════════════════════════════
router.post("/", requireAdmin, async (req, res) => {
  const generatePaths = req.query.generatePaths === "true";
  const section = (req.query.section as string) || "all";
  const results: string[] = [];

  try {
    // 1. Upsert transport types
    const typeMap: Record<string, string> = {};
    for (const t of TYPES) {
      const ex = await db.select().from(transportTypesTable)
        .where(eq(transportTypesTable.nameEn, t.nameEn)).limit(1);
      if (ex.length > 0) {
        typeMap[t.nameEn] = ex[0].id;
      } else {
        const [ins] = await db.insert(transportTypesTable).values({
          nameEn: t.nameEn, nameAr: t.nameAr, icon: t.icon, color: t.color,
          basePriceEgp: t.base, pricePerKmEgp: t.pkm, averageSpeedKmh: t.spd,
          foreignerAllowed: true, isActive: true,
        }).returning();
        typeMap[t.nameEn] = ins.id;
        results.push(`Created type: ${t.nameEn}`);
      }
    }

    // 2. Collect line specs by section
    const allLines: LineSpec[] = [];
    if (section === "all" || section === "metro")    allLines.push(...METRO_LINES);
    if (section === "all" || section === "monorail") allLines.push(...MONORAIL_LINES);
    if (section === "all" || section === "train")    allLines.push(...TRAIN_LINES);
    if (section === "all" || section === "nta")      allLines.push(...NTA_LINES);
    if (section === "all" || section === "serfis")   allLines.push(...SERFIS_LINES);

    // 3. Seed each line
    for (const line of allLines) {
      const typeId = typeMap[line.typeKey];
      if (!typeId) { results.push(`No type for: ${line.typeKey}`); continue; }

      const ex = await db.select().from(transitLinesTable)
        .where(eq(transitLinesTable.lineNumber, line.lineNum)).limit(1);
      if (ex.length > 0) { results.push(`Skip: ${line.lineNum}`); continue; }

      let routePath = null;
      if (generatePaths && !line.hasFixedStops) {
        try {
          routePath = (await buildBusRoutePathAI(line.from, line.to, line.via, "Cairo")).routePath;
        } catch { /* continue without path */ }
      }

      await db.insert(transitLinesTable).values({
        transportTypeId: typeId,
        lineNumber: line.lineNum,
        nameEn: line.nameEn,
        nameAr: line.nameAr,
        fromArea: line.from,
        toArea: line.to,
        viaStops: line.via,
        priceEgp: line.price,
        isActive: true,
        frequencyMinutes: line.freq,
        hasFixedStops: line.hasFixedStops,
        routePath: routePath,
      });
      results.push(`Seeded: ${line.lineNum}${generatePaths && routePath ? " ✓path" : ""}`);
    }

    const seeded = results.filter(r => r.startsWith("Seeded")).length;
    const skipped = results.filter(r => r.startsWith("Skip")).length;
    res.json({ success: true, seeded, skipped, total: allLines.length, results });
  } catch (err: unknown) {
    const msg = err instanceof Error ? err.message : "Unknown error";
    console.error("Cairo seed error:", err);
    res.status(500).json({ error: msg });
  }
});

export default router;
